    // Simple pool with simple excludes
    public function testParticipantShufflerCase1()
    {
        $party = new Party();
        foreach ($party->getParticipants() as $defaultEntry) {
            $party->removeParticipant($defaultEntry);
        }

        $entry1 = new Participant();
        $entry1->setName('Entry 1');
        $party->addParticipant($entry1);

        $entry2 = new Participant();
        $entry2->setName('Entry 2');
        $party->addParticipant($entry2);

        $entry3 = new Participant();
        $entry3->setName('Entry 3');
        $party->addParticipant($entry3);

        $entry4 = new Participant();
        $entry4->setName('Entry 4');
        $party->addParticipant($entry4);

        $entry1->addExcludedParticipant($entry2);
        $entry2->addExcludedParticipant($entry3);
        $entry4->addExcludedParticipant($entry1);
        $entry4->addExcludedParticipant($entry2);

        $ParticipantShuffler = new ParticipantShuffler();
        for ($i = 0; $i < 1000; ++$i) {
            $shuffeledEntries = $ParticipantShuffler->shuffleParticipants($party);

            $key = 0;
            foreach ($party->getParticipants() as $entry) {
                //check if we did not match excluded entry
                $this->assertNotContains($shuffeledEntries[$key], $entry->getExcludedParticipants());
                //check if we have an entry matched
                $this->assertNotNull($shuffeledEntries[$key]);
                ++$key;
            }
        }
    }

